# Function in Python

# 1. Basic function
def greet():
    print("Hello, World!")

greet()

# 2. Function with parameters
def add(a, b):
    return a + b

result = add(5, 3)
print("Sum:", result)

# 3. Function with default parameter
def power(base, exponent=2):
    return base ** exponent

print("Power:", power(3))
print("Power with exponent:", power(3, 3))


